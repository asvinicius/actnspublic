							e o respeito dos participantes. Foi também neste ano que teve início o Bolão Acretinos, uma disputa tiro-curto por rodada que 
							ganhou a simpatia dos participantes da liga, se tornando, em 2019, uma das competições mais importantes da liga.
						</p>
